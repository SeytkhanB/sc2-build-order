import { type NextPage } from "next";
import Head from "next/head";
import { useRouter } from "next/router";
import React, { useState } from "react";
import DropDown from "../../../../../components/DropDown";
import Input from "../../../../../components/Input";
import Label from "../../../../../components/Label";

const Final: NextPage = () => {
  const [searchState, setSearchState] = useState("");

  const { opponentRace, raceName } = useRouter().query as {
    opponentRace: string;
    raceName: string;
  };

  const handleChange = (e: React.ChangeEvent<HTMLInputElement>) => {
    setSearchState(e.target.value);
  };

  return (
    <>
      <Head>
        <title>Create T3 App</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/sc2.png" />
      </Head>
      <main
        className="flex min-h-screen 
        flex-col items-center justify-center gap-4 bg-gradient-to-b 
        from-[#2e026d] to-[#15162c] text-white"
      >
        <h2 className="text-3xl text-white">
          {raceName} vs {opponentRace}
        </h2>

        <form>
          <fieldset>
            <Label labelFor="search">Search</Label>
            <Input
              placeholder="Search"
              handleChange={handleChange}
              value={searchState}
              id="search"
            />
          </fieldset>
        </form>

        <DropDown raceName={raceName} opponentRace={opponentRace} />
      </main>
    </>
  );
};

export default Final;
